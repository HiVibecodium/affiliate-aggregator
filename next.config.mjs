  widenClientFileUpload: true,
  hideSourceMaps: true,
  disableLogger: true,

  // Reduce bundle size
  tunnelRoute: '/monitoring',
  autoInstrumentServerFunctions: false,
  autoInstrumentMiddleware: false,
