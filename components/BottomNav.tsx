export function BottomNav() {
  const pathname = usePathname();
  const t = useTranslations('nav');

  const isActive = (href: string) => {
    if (href === '/') return pathname === '/';
    if (href === '/settings') {
      return ['/settings', '/dashboard', '/applications', '/analytics'].some((p) =>
        pathname.startsWith(p)
      );
    }
    return pathname.startsWith(href);
  };

  return (
    <>
      {/* Spacer to prevent content from being hidden behind bottom nav */}
      <div className="h-16 md:hidden" />

      {/* Bottom Navigation */}
      <nav className="fixed bottom-0 left-0 right-0 z-50 md:hidden bg-white dark:bg-gray-900 border-t border-gray-200 dark:border-gray-700 safe-area-bottom">
        <div className="flex items-center justify-around h-16">
          {navItemsConfig.map((item) => {
            const active = isActive(item.href);
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex flex-col items-center justify-center flex-1 h-full min-w-0 px-1 transition-colors touch-manipulation ${
                  active
                    ? 'text-blue-600 dark:text-blue-400'
                    : 'text-gray-500 dark:text-gray-400 active:text-gray-700 dark:active:text-gray-200'
                }`}
              >
                <span className="relative">
                  {active ? item.activeIcon || item.icon : item.icon}
                  {/* Notification badge example - can be made dynamic */}
                  {item.href === '/favorites' && (
                    <span className="absolute -top-1 -right-1 w-2 h-2 bg-red-500 rounded-full hidden" />
                  )}
                </span>
                <span className={`text-xs mt-1 truncate max-w-full ${active ? 'font-medium' : ''}`}>
                  {t(item.labelKey)}
                </span>
              </Link>
            );
          })}
        </div>
      </nav>
    </>
  );
}
