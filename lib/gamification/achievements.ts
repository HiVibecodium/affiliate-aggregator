export function getAchievementsByCategory(category: AchievementCategory): Achievement[] {
  return ACHIEVEMENTS.filter((a) => a.category === category && !a.isSecret);
}

export function getAchievementById(id: string): Achievement | undefined {
  return ACHIEVEMENTS.find((a) => a.id === id);
}

export function getAchievementByCode(code: string): Achievement | undefined {
  return ACHIEVEMENTS.find((a) => a.code === code);
}

export function getTierColor(tier: AchievementTier): string {
  const colors: Record<AchievementTier, string> = {
    bronze: '#CD7F32',
    silver: '#C0C0C0',
    gold: '#FFD700',
    platinum: '#E5E4E2',
    diamond: '#B9F2FF',
  };
  return colors[tier];
}

export function getTierBgClass(tier: AchievementTier): string {
  const classes: Record<AchievementTier, string> = {
    bronze: 'bg-orange-100 dark:bg-orange-900 border-orange-300',
    silver: 'bg-gray-100 dark:bg-gray-700 border-gray-300',
    gold: 'bg-yellow-100 dark:bg-yellow-900 border-yellow-300',
    platinum: 'bg-purple-100 dark:bg-purple-900 border-purple-300',
    diamond: 'bg-cyan-100 dark:bg-cyan-900 border-cyan-300',
  };
  return classes[tier];
}
